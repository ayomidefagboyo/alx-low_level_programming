/**
 * op_add - sums to inputs and returns sum as output.
 * @a: one int.
 * @b: 2nd int.
 * Return: int which is sum of two inputs.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two inputs.
 * @a: 1st int.
 * @b: 2nd int.
 * Return: the diff of two inputs.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - get product of two inputs.
 * @a: 1st int.
 * @b: 2nd int.
 * Return: the product of two ints.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two input ints.
 * @a: 1st int.
 * @b: 2nd int.
 * Return: the result of dividing 2 ints.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder of two numbers.
 * @a: 1st int.
 * @b: 2nd int.
 * Return: remainder when two numbers are divided.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
